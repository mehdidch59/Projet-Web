class Housing {     // Housing Model

    constructor(title, city, zipcode, area, rooms, level, price, photo) {
        this.title = title;
        this.city = city;
        this.zipcode = zipcode;
        this.area = area;
        this.rooms = rooms;
        this.level = level;
        this.price = price;
        this.photo = photo
    };

}

module.exports = Housing